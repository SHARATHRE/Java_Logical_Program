import java.util.*;
class even
  {
    public static void main(String args[])
    {
      float A;
      
      Scanner sc=new Scanner(System.in);
      
      System.out.println("Enter the value of A = ");
      A=sc.nextFloat();
      
 

    if(A%2==0)
    {
   System.out.println("The number is even");     
    }
    else
    {
   System.out.println("The number is not even");
    }
  }
}